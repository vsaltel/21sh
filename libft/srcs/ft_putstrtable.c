#include "libft.h"

void	ft_putstrtable(char **tab)
{
	int i;

	if (!tab)
		return ;
	i = 0;
	while (tab[i] != 0)
	{
		write(1, tab[i], ft_strlen(tab[i]));
		write(1, "\n", 1);
		i++;
	}
}
